# Listas

# Lista de nombres de meses del año del primer trimestre
meses = ["Enero", "Febrero", "Marzo"]

# Imprimir lista de elementos
print(meses);
      
# Agregar elementos de mes
meses.append("Abril")

# Imprimir la lista de elementos actualizado
print(meses)

# Agregar un elemento de número
meses.append(123)

print(meses)

# Agregar los meses hasta el mes de Octubre
meses.extend(["Mayo", "Junio", "Julio", "Agosto", "Setiembre", "Octubre"])
print(meses)