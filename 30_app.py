# Lista

# Listas anidadas
estudiantes = [["Fabiola", 21], ["Pablo", 15], ["Angela", 20]]

# Imprimir elementos de lista
print(estudiantes)

# Imprimiendo elemento de posición 0
print(estudiantes[0])

# Imprimiendo los 2 últimos elementos de la lista
print(estudiantes[1:3])

# Obtener la cantidad de elementos que tiene una lista
print(len(estudiantes))